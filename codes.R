# Simulation function for k = 3

comp3 <- function(n1, n2, n3, 
                  m1, m2, m3, 
                  s1, s2, s3, reptt){
  
  k3 <- function(n1, n2, n3, m1, m2, m3, s1, s2, s3){
    samp <- data.frame(data = c(rnorm(n1, m1, s1), rnorm(n2, m2, s2), rnorm(n3, m3, s3)),
                       grp = c(rep(1, n1), rep(2, n2), rep(3, n3)))
    
    res <- c(AG(samp$data, samp$grp)[3],
             AF(samp$data, samp$grp)[4],
             B2(0.05, samp$data, samp$grp)[3],
             GF(samp$data, samp$grp)[1],
             JF(samp$data, samp$grp)[4],
             MBF(samp$data, samp$grp)[4],
             PB(samp$data, samp$grp)[1],
             WE(samp$data, samp$grp)[3],
             WA(samp$data, samp$grp)[3])
    return(res) 
  }
  registerDoMC(cores = detectCores() - 1)
  set.seed(123, kind = "L'Ecuyer-CMRG")
  resp <- foreach(c(1:reptt), .combine = 'rbind') %dopar%  k3(n1, n2, n3, m1, m2, m3, s1, s2, s3)
  colnames(resp) <- c("AG", "AF", "B2", "GF", "JF", "MBF", "PB", "WE", "WA")
  rownames(resp) <- NULL
  return(resp)
}

